export function detectLang() {
  try {
    const saved = localStorage.getItem('sirago_booking_lang');
    if (saved && TRANSLATIONS[saved]) return saved;
  } catch {}
  const nav = (navigator.language || 'tr').toLowerCase().slice(0, 2);
  return TRANSLATIONS[nav] ? nav : 'tr';
}

export function saveLang(kod) {
  try { localStorage.setItem('sirago_booking_lang', kod); } catch {}
}
